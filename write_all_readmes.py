import os, re

BASE = os.path.join(os.path.dirname(__file__), 'notes')

def update_readme(dirname, algo_name, time_c, space_c, steps, js_only=False):
    path = os.path.join(BASE, dirname, 'README.md')
    with open(path, 'r') as f:
        content = f.read()

    # Find the start of section 2
    match = re.search(r'^## 2\. 🎯 s\.1.*$', content, re.MULTILINE)
    if not match:
        print(f"WARNING: No s.1 section found in {dirname}")
        return

    before = content[:match.start()]

    if js_only:
        code_group = """::: code-group

<<< ./solutions/1/1.js [js]

:::"""
    else:
        code_group = """::: code-group

<<< ./solutions/1/1.js [js]
<<< ./solutions/1/1.c [c]
<<< ./solutions/1/1.py [py]

:::"""

    new_section = f"""## 2. 🎯 s.1 - {algo_name}

{code_group}

- 时间复杂度：{time_c}
- 空间复杂度：{space_c}

算法思路：

{steps}
"""

    with open(path, 'w') as f:
        f.write(before + new_section)

